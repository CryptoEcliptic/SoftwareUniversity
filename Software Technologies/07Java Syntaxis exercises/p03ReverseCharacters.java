public class p03ReverseCharacters {

    public  static void main(String[] args){

        Scanner scan = new Scanner(System.in);

        String n1 = scan.nextLine();
        String n2 = scan.nextLine();
        String n3 = scan.nextLine();

        System.out.print(n3+n2+n1);}


}
